package oc.p._8._3_GenericsAndCollections.searchingAndSorting.sortAndBinarySearch;

class Tmp {

    private String name;

    public Tmp(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Tmp{" +
                "name='" + name + '\'' +
                '}';
    }
}
