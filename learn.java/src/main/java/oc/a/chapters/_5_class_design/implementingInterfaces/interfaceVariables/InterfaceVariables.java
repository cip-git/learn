package oc.a.chapters._5_class_design.implementingInterfaces.interfaceVariables;

/**
 * Interface variables are assumed to be public, static, and final
 * 
 * The value of an interface variable must be set when it is declared since it
 * is marked as final.
 *
 */
public interface InterfaceVariables {

	public static final int IA = 3;
	
	int IB = 23;
}
